#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	len;

	len = 0;
	if (n == -2147483648)
	{
		len += ft_putstr("-2147483648");
		return (len);
	}
	if (n < 0)
	{
		len += ft_putchar('-');
		n = -n;
	}
	if (n > 9)
		len += ft_putnbr(n / 10);
	len += ft_putchar((n % 10) + '0');
	return (len);
}

int	ft_unsignednbr(unsigned int n)
{
	int	len;

	len = 0;
	if (n > 9)
		len += ft_unsignednbr(n / 10);
	len += ft_putchar((n % 10) + '0');
	return (len);
}
